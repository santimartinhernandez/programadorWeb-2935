var genero
var edad
var message

genero = prompt('Ingresa tu genero')
edad = prompt('¿Cuántos años tenes?')

switch (genero) {
  case 'male':
  case 'hombre':
  case 'masculino':
    genero = 'Sr.'
    break

  case 'female':
  case 'mujer':
  case 'femenino':
    genero = 'Sra.'
    break

  default:
    // GENERO NO BINARIO
    genero = 'Sx.'
    break
}

if (edad >= 18) {
  edad = 'mayor de edad puede ingresar'
} else {
  edad = 'menor de edad no puede ingresar'
}

console.log(genero + 'usted es ' + edad)

